# This program performs basic arithmetic operations on two numbers provided by the user.
# It calculates the sum, difference, product, and quotient of the two numbers.
num1 = int(input("Enter first number: "))
num2 = int(input("Enter second number: "))
sum = num1 + num2
subtract = num1 - num2
multiply = num1 * num2
divide = num1 / num2
print("Sum of two numbers is:", sum)
print("Subtraction of two numbers is:", subtract)
print("Multiplication of two numbers is:", multiply)
print("Division of two numbers is:", divide)